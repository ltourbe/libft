#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const unsigned char	*st1;
	const unsigned char	*st2;
	size_t				i;

	st1 = s1;
	st2 = s2;
	if (n == 0)
		return (0);
	i = 0;
	while (i < n)
	{
		if (st1[i] != st2[i])
			return ((int)(st1[i] - st2[i]));
		i++;
	}
	return (0);
}
